
public class Point3D {
	
	private double x, y, z;	// x, y, and z components of the 3d point
	
	public Point3D(double x, double y, double z) {
		
		this.x = x;
		this.y = y;
		this.z = z;
		
	}
	
	/* GETTERS */
	
	public double getX() {
		return x;
	}
	
	public double getY() {
		return y;
	}
	
	public double getZ() {
		return z;
	}

}
